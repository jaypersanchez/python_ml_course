# If-Else Example
age = 18
if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# For Loop Example
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# While Loop Example
count = 0
while count < 5:
    print(count)
    count += 1

# Function Example
def greet(name):
    print(f"Hello, {name}!")

greet("Alice")
